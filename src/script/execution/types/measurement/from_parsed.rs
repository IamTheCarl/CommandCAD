fn replace_symbols(input: &str) -> String {
    let to_replace = [
        ("¹", "^1"),
        ("²", "^2"),
        ("³", "^3"),
        ("⁴", "^4"),
        ("⁻¹", "^m1"),
        ("⁻²", "^m2"),
        ("⁻³", "^m3"),
        ("⁻⁴", "^m4"),
        ("₀", "\\0"),
        ("₂", "\\2"),
        ("₃", "\\3"),
        ("₅", "\\5"),
        ("₆", "\\6"),
        ("₉", "\\9"),
        (" ", "_"),
        (" · ", "*"),
        (" ⋅ ", "*"),
        ("°", "deg"),
        ("′", "angle_minute"),
        ("″", "angle_second"),
        ("ε", "epsilon"),
        ("℧", "mho"),
        ("Ω", "ohm"),
        ("µ", "u"),
        ("μ", "u"),
        ("Å", "ang"),
        ("’", "'"),
        ("‰", "%\\0"),
        ("∞", "infinity"),
        ("ₕ", "\\h"),
        ("ħ", "n<-"),
        ("ₑ", "\\e"),
    ];

    let mut output = String::from(input);

    for (from, to) in to_replace.iter() {
        output = output.replace(from, to);
    }

    output
}

macro_rules! build_lookup_tables {
    ( $( $quantity:ident [ $($($unit:ident)::*,)* ] ),* ) => {
	lazy_static::lazy_static! {
	    static ref CONVERT_FLOAT_TO_MEASUREMENT: HashMap<String, fn(f64) -> Measurement> = {
		HashMap::from([
		    $( $(
			(replace_symbols($($unit)::* ::abbreviation()), (|value: f64| -> Measurement { Measurement::try_from( uom::si::f64:: $quantity ::new::< $($unit)::* >( value ) ).unwrap() } as _)),
		    )* )*
		])
	    };
	    static ref CONVERT_MEASUREMENT_TO_FLOAT: HashMap<String, fn(&Measurement) -> Option<f64>> = {
		HashMap::from([
		    $( $(
			(replace_symbols($($unit)::* ::abbreviation()), (|value: &Measurement| -> Option<f64> {
			    let measurement: uom::si::f64:: $quantity = value.clone().try_into().ok()?;

			    Some(measurement.get::< $($unit)::* >())
			} as _)),
		    )* )*
		])
	    };
	}
    };
}

impl Measurement {
    pub fn from_parsed<'a, S: Span>(
        measurement: &parsing::Measurement<S>,
    ) -> OperatorResult<S, Value<'a, S>> {
        if let Some(converter) = CONVERT_FLOAT_TO_MEASUREMENT.get(measurement.ty.as_str()) {
            let value = unwrap_float(measurement.number.get_span().clone(), &measurement.number)?;

            Ok(converter(value.into_inner()).into())
        } else {
            Err(Failure::UnknownUnitType(
                measurement.ty.clone(),
                measurement.ty.to_string().into(),
            ))
        }
    }

    pub fn from_number<'a, S: Span>(
        span: &S,
        value: Number,
        ty: SString,
    ) -> OperatorResult<S, Value<'a, S>> {
        if let Some(converter) = CONVERT_FLOAT_TO_MEASUREMENT.get(ty.as_str()) {
            Ok(converter(value.into_inner()).into())
        } else {
            Err(Failure::UnknownUnitType(
                span.clone(),
                ty.into_string().into(),
            ))
        }
    }

    pub fn to_number<'a, S: Span>(&self, span: &S, ty: &str) -> OperatorResult<S, Value<'a, S>> {
        if let Some(converter) = CONVERT_MEASUREMENT_TO_FLOAT.get(ty) {
            if let Some(converted) = converter(self) {
                Number::new(converted).unwrap_not_nan(span)
            } else {
                Err(Failure::CannotConvertFromTo(
                    span.clone(),
                    Object::<S>::type_name(self),
                    ty.to_string().into(),
                ))
            }
        } else {
            Err(Failure::UnknownUnitType(
                span.clone(),
                ty.to_string().into(),
            ))
        }
    }
}

